class UserEntity {
  final String fullName;
  final String rg;
  final String cpf;
  final String email;
  final String password;
  final String profilePictureUrl;
  final String birthDate;


  UserEntity({
    required this.fullName,
    required this.rg,
    required this.cpf,
    required this.email,
    required this.password,
    required this.profilePictureUrl,
    required this.birthDate,
  });
}
